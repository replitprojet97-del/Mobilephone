function updatePageContent() {
    // Mettre Ã  jour tous les Ã©lÃ©ments avec des attributs data-translate
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = t(key);
        
        if (element.tagName === 'INPUT' && element.type === 'text') {
            element.placeholder = translation;
        } else {
            element.textContent = translation;
        }
    });
    
    // Mettre Ã  jour tous les Ã©lÃ©ments avec des attributs data-translate-placeholder
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        const translation = t(key);
        
        if (element.placeholder !== undefined) {
            element.placeholder = translation;
        }
    });
    
    // Mettre Ã  jour le sÃ©lecteur de langue actuel
    updateLanguageSelector();
}

function updateLanguageSelector() {
    const currentLangElement = document.querySelector('.current-language');
    if (currentLangElement) {
        const config = languageConfig[currentLanguage];
        currentLangElement.innerHTML = `${config.flag} ${config.name}`;
    }
}

